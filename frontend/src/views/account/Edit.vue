import axios from 'axios';

export default {
    name: "Index",

    data() {
        return {
            scriptElements: [], // Массив для хранения загруженных скриптов

            user: null,

            show: false,
            qr: null,
            shouldHighlight: false
        };
    },

    mounted() {

        // add scripts
        const scripts = [
            '../src/assets/js/bootstrap.min.js',
            '../src/assets/js/jquery.min.js',
            '../src/assets/js/swiper-bundle.min.js',
            '../src/assets/js/carousel.js',
            '../src/assets/js/bootstrap-select.min.js',
            '../src/assets/js/lazysize.min.js',
            '../src/assets/js/count-down.js',
            '../src/assets/js/wow.min.js',
            '../src/assets/js/multiple-modal.js',
            '../src/assets/js/main.js'
        ];

        const loadScript = (src) => {
            return new Promise((resolve, reject) => {
                const script = document.createElement('script');
                script.src = src;
                script.async = true;

                script.onload = () => {
                    this.scriptElements.push(script); // Сохраняем ссылку на скрипт
                    resolve();
                };
                script.onerror = () => reject(new Error(`Ошибка загрузки скрипта: ${src}`));

                document.body.appendChild(script);
            });
        };

        const loadScriptsSequentially = async () => {
            for (const src of scripts) {
                try {
                    await loadScript(src);
                } catch (error) {
                    console.error(error);
                }
            }
        };

        loadScriptsSequentially();

        this.getUser();

        axios.defaults.withCredentials = true
    },

    beforeUnmount() {
        this.scriptElements.forEach(script => {
            document.body.removeChild(script); // Удаляем каждый скрипт из DOM
        });
        this.scriptElements = []; // Очищаем массив
    },

    methods: {
        getUser() {
            const xsrfToken = this.getCookieValue('XSRF-TOKEN');
            axios.get('/sanctum/csrf-cookie', {
                    withCredentials: true
                })
                .then(res => {
                    axios.get('/api/user', {
                            withCredentials: true,
                            headers: {
                                'X-XSRF-TOKEN': decodeURIComponent(xsrfToken)
                            }
                        })
                        .then(res => {
                            this.user = res.data; // например, сохранить в стейт

                            this.userCheckTgChat()
                        })
                        .catch(err => {
                            console.error('Not authenticated:', err);
                            this.$router.push({
                                name: 'account.login'
                            })
                        });
                })
        },
        fetchQr() {
            const xsrfToken = this.getCookieValue('XSRF-TOKEN');
            axios.get('/sanctum/csrf-cookie', {
                    withCredentials: true
                })
                .then(res => {
                    axios.get('/api/telegram/qr', {
                            withCredentials: true,
                            headers: {
                                'X-XSRF-TOKEN': decodeURIComponent(xsrfToken)
                            }
                        })
                        .then(response => {
                            // Получаем qr-строку из response.data
                            this.qr = response.data.qr;
                        })
                        .catch(err => {
                            console.error('Ошибка при получении QR:', err);
                            this.closeModal();
                            alert('Не удалось получить QR-код. Проверьте авторизацию и правильность URL.');
                        });
                })
        },

        updateUser() {
            let id = this.user.id
            const xsrfToken = this.getCookieValue('XSRF-TOKEN');
            axios.get('/sanctum/csrf-cookie', {
                    withCredentials: true
                })
                .then(response => {
                    axios.patch(`/api/user/${id}`, {
                            name: this.user.name,
                            surname: this.user.surname,
                            address: this.user.address,
                            phone: this.user.phone,
                        }, {
                            withCredentials: true,
                            headers: {
                                'X-XSRF-TOKEN': decodeURIComponent(xsrfToken)
                            }
                        })
                        .then(res => {

                        })
                        .catch(err => {
                            // this.$router.push({
                            //     name: 'account.login'
                            // })
                        });
                })
        },

        connectToTelegram() {
            axios.get('/api/telegram/deepLink')
                .then(res => {
                    window.open(res.data.url)

                })
        },

        getCookieValue(name) {
            const value = `; ${document.cookie}`;
            const parts = value.split(`; ${name}=`);
            if (parts.length === 2) return parts.pop().split(';').shift();
        },

        userCheckTgChat() {
            setTimeout(() => {
                if (!this.user.telegram_chat_id) {
                    this.$refs.tgBtn.click()
                }
            }, 1500);
        },

        logout() {
            const xsrfToken = this.getCookieValue('XSRF-TOKEN');
            axios.get('/sanctum/csrf-cookie', {
                    withCredentials: true
                })
                .then(response => {
                    axios.post('/api/logout', {}, {
                            withCredentials: true,
                            headers: {
                                'X-XSRF-TOKEN': decodeURIComponent(xsrfToken)
                            }
                        })
                        .then(res => {
                            console.log('logout sec');
                            
                            this.$router.push({
                                name: 'main'
                                
                            })

                        })
                        .catch(err => {

                        });
                })
        }
    }
